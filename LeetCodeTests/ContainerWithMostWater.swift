import XCTest

class ContainerWithMostWater: XCTestCase {

    class Solution {
        func maxArea(_ height: [Int]) -> Int {
            var maxArea: Int = 0
            var i = 0
            var j = height.count - 1
            while i < j {
                var minHeight = 0
                let left = height[i]
                let right = height[j]
                
                if left < right {
                    minHeight = left
                    i += 1
                }else{
                    minHeight = right
                    j -= 1
                }
                maxArea = max(maxArea, (j - i + 1) * minHeight)
            }
            return maxArea
        }
    }

